package Herencia;

public class Persona {
	private String nombre;
	private String apelllido;
	private int edad;
	
	public Persona(String nombre, String apelllido, int edad) {
		this.nombre = nombre;
		this.apelllido = apelllido;
		this.edad = edad;
	}

	public String getNombre() {
		return nombre;
	}

	public String getApelllido() {
		return apelllido;
	}

	public int getEdad() {
		return edad;
	}

	
	
	
	
	
	
	

}
